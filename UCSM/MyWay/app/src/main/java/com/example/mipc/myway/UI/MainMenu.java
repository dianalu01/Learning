package com.example.mipc.myway.UI;

import android.content.Intent;
import android.os.Bundle;
import android.support.v7.app.AppCompatActivity;
import android.view.View;

public class MainMenu extends AppCompatActivity {

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.main_menu);
    }
    public void PasarRetosDia (View view){
        Intent siguiente= new Intent(this,DailyChallenge.class);
        startActivity(siguiente);
    }
    public void PasarRetosSuperadoss (View view){
        Intent siguiente= new Intent(this,AchievedChallenge.class);
        startActivity(siguiente);
    }
    public void PasarPremios (View view){
        Intent siguiente= new Intent(this,Rewards.class);
        startActivity(siguiente);
    }
    public void PasarConfiguraciones (View view){
        Intent siguiente= new Intent(this,MainMenu.class);
        startActivity(siguiente);
    }
    public void CerrarSesion (View view){
        Intent siguiente= new Intent(this,LogIn.class);
        startActivity(siguiente);
    }
    public void PasarMenu (View view){
        Intent siguiente= new Intent(this,MainMenu.class);
        startActivity(siguiente);
    }


}