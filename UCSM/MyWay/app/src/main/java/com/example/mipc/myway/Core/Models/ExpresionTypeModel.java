package com.example.mipc.myway.Core.Models;


public class ExpresionTypeModel {
    private String IdTipo;
    private String DescripcionTipoExpresion;
    private double Puntaje;

    public String getIdTipo() {        return IdTipo;    }
    public void setIdTipo(String idTipo) {IdTipo = idTipo;}
    public String getDescripcionTipoExpresion() {return DescripcionTipoExpresion;}
    public void setDescripcionTipoExpresion(String descripcionTipoExpresion) {DescripcionTipoExpresion = descripcionTipoExpresion;}
    public double getPuntaje() {return Puntaje;}
    public void setPuntaje(double puntaje) {Puntaje = puntaje;}

    public ExpresionTypeModel(){}
    public ExpresionTypeModel(String idTipo,String descripcionTipoExpresion,double puntaje)
    {
        IdTipo=idTipo;
        DescripcionTipoExpresion=descripcionTipoExpresion;
        Puntaje=puntaje;
    }

}