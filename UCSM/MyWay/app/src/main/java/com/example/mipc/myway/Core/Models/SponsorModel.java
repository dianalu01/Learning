package com.example.mipc.myway.Core.Models;


public class SponsorModel {
   	private int IdSponsor;
	private String Descripcion;
	private String Direccion;
	private String Telefono;
 
   
    public int getIdSponsor (){return IdSponsor;}
    public String getDescripcion (){return Descripcion;}
	public String getDireccion (){return Direccion;}
    public String getTelefono (){return Telefono;}
	
	public void setIdSponsor(int IdSponsor){this.IdSponsor=IdSponsor;}
    public void setDescripcion(String Descripcion){this.Descripcion=Descripcion;}
	public void setDireccion(String Direccion){this.Direccion=Direccion;}
    public void setTelefono(String Telefono){this.Telefono=Telefono;}

    public SponsorModel(){}
    public SponsorModel( int IdSponsor, String Descripcion,String Direccion ,String Telefono)
    {
        this.IdSponsor=IdSponsor;
		this.Descripcion=Descripcion; 
		this.Direccion=Direccion;		
		this.Telefono=Telefono;
    }
}
