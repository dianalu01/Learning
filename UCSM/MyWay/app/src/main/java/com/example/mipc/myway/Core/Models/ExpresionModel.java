package com.example.mipc.myway.Core.Models;

public class ExpresionModel {
    private String IdExpresion;
    private String DescripcionExpresion;
    private String IdTipo;

    public String getIdExpresion (){return this.IdExpresion;}
    public void setIdExpresion(String id){this.IdExpresion=id;}
    public String getdescExpresion(){return this.DescripcionExpresion;}
    public void setDescExpresion (String desc) {this.DescripcionExpresion=desc;}
    public String getIdTipo() {        return IdTipo;    }
    public void setIdTipo(String idTipo) {IdTipo = idTipo;}

    public ExpresionModel(){}
    public ExpresionModel(String idExpresion, String descExpresion, String idTipo)
    {
        IdExpresion = idExpresion;
        DescripcionExpresion = descExpresion;
        IdTipo=idTipo;
    }
}